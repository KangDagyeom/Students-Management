    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        if (selectedImagePath == null || selectedImagePath.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please upload an image before inserting.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String fullName = txtfullname.getText();
        String userName = txtusername.getText();
        String password = txtpassword.getText();
        String email = txtemail.getText();
        Date birthDate = Date.valueOf(txtbirth.getText());
        Boolean isActive;
        if (rdtrue.isSelected()) {
            isActive = true;
        } else {
            isActive = false;
        }
        try {
            User u = new User(fullName, userName, password, email, birthDate, isActive);
            bQuery.insertUser(u);
            al.clear();
            al = bQuery.getList();
            loadData(al);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btninsertActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        lbimg.setPreferredSize(new Dimension(200, 100)); // Kích thước cố định 200x200
        lbimg.setSize(200, 100); // Áp dụng kích thước

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select an image");
        int result = fileChooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String destinationPath = "C:\\Users\\Hyun\\Documents\\NetBeansProjects\\TestASMJava3\\src\\Images\\user-images\\" + selectedFile.getName();
            try {
                // Lưu ảnh vào thư mục
                Files.copy(selectedFile.toPath(), Paths.get(destinationPath), StandardCopyOption.REPLACE_EXISTING);
                System.out.println("Image saved: " + destinationPath);
                selectedImagePath = destinationPath;
                // Buộc JLabel cập nhật kích thước
                lbimg.revalidate();
                lbimg.repaint();

                // Kiểm tra kích thước JLabel
                if (lbimg.getWidth() > 0 && lbimg.getHeight() > 0) {
                    ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());
                    Image scaledImage = imageIcon.getImage().getScaledInstance(lbimg.getWidth(), lbimg.getHeight(), Image.SCALE_SMOOTH);
                    lbimg.setIcon(new ImageIcon(scaledImage));
                } else {
                    System.out.println("Warning: JLabel dimensions are not set properly.");
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

