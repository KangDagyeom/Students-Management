    public void loadData(ArrayList<User> al) {
        defaultTableModel = (DefaultTableModel) tbluser.getModel();
        defaultTableModel.setRowCount(0);
        int id = 1;
        for (User user : al) {
            defaultTableModel.addRow(new Object[]{
                id,
                user.getFullName(),
                user.getUsername(),
                user.getPassword(),
                user.getEmail(),
                user.getBirthDate(),
                user.getIsActive() ? "Online" : "Offline"
            });
            id++;
        }
    }

